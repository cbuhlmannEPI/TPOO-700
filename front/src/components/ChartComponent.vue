
<template>
  <h2>CHART</h2>
  <div class="chart">
    <div class="card">
      <Pie :chart-options="chartOptions" :chart-data="chartData" :chart-id="chartId" :dataset-id-key="datasetIdKey"
        :plugins="plugins" :css-classes="cssClasses" :styles="styles" :width="width" :height="height"></Pie>
    </div>
    <div class="card">
      <Line :chart-options="chartOptions" :chart-data="chartData" :chart-id="chartId" :dataset-id-key="datasetIdKey"
        :plugins="plugins" :css-classes="cssClasses" :styles="styles" :width="width" :height="height"></Line>
    </div>
    <div class="card">
      <Bar :chart-options="chartOptions" :chart-data="chartData" :chart-id="chartId" :dataset-id-key="datasetIdKey"
        :plugins="plugins" :css-classes="cssClasses" :styles="styles" :width="width" :height="height"></bar>
    </div>
  </div>
</template>


<script>


import {
  defineComponent,
} from 'vue'
import moment from 'moment';
import axios from 'axios';
import { Pie, Line, Bar } from 'vue-chartjs'
import {
  Chart as ChartJS,
  Title,
  Tooltip,
  Legend,
  ArcElement,
  CategoryScale,
  LineElement,
  LinearScale,
  PointElement,
  BarElement

} from 'chart.js'

ChartJS.register(Title, Tooltip, Legend, ArcElement, CategoryScale, LineElement, BarElement,
  LinearScale,
  PointElement)

export default defineComponent({
  name: 'PieChart',
  components: {
    Pie,
    Line,
    Bar
  },
  props: {
    chartId: {
      type: String,
      default: 'pie-chart'
    },
    lineId: {
      type: String,
      default: 'line-chart'
    },
    width: {
      type: Number,
      default: 290
    },
    height: {
      type: Number,
      default: 290
    },
    cssClasses: {
      default: '',
      type: String
    },
    styles: {
      default: () => { }
    },
    plugins: {
      type: Array,
      default: () => []
    }
  }, data() {
    return {
      chartData: {
        labels: ['test', 'test', 'test', 'test'],
        datasets: [
          {
            label: 'Working Time',
            backgroundColor: ['#1d3557', '#457b9d', '#a8dadc', '#f1faee', '#e63946'],
            data: [40, 20, 30, 10, 100, 150]
          }]
      },
      chartOptions: {
        responsive: false,
      },
    }
  },
  methods: {
    formatDate(value) {
      return moment(String(value)).format('YYYY-MM-DD')
    }
  },
  created() {
    axios
      .get(`http://localhost:4000/api/workingtimes/` + sessionStorage['userID'])
      .then((response) => {
        response.data.data.forEach(wtime => {
          let start = this.formatDate(wtime.start);
          // let end = this.formatDate(wtime.end)
          this.chartData.labels.push(start)
        });
      })
      .catch((errors) => {
        console.log(errors)
      });
  }
})
</script>

